def main() -> int:
    profile = os.environ.get("CANDIDATE2060_PROFILE", "compare100").strip().lower()
    if profile not in PROFILES:
        print(
            f"CANDIDATE2060_PROFILE must be one of {', '.join(sorted(PROFILES))}, got {profile!r}",
            file=sys.stderr,
        )
        return 2
    if not TRAINER.is_file():
        print(f"Missing CUDA trainer wrapper: {TRAINER}", file=sys.stderr)
        return 1
    if not DATASET_DIR.is_dir() or not TOKENIZER_PATH.is_file():
        print(
            "Missing CaseOps data/tokenizer. First run the upstream downloader:\n"
            "  MATCHED_FINEWEB_REPO_ID=romeerp/parameter-golf-caseops-v1\n"
            "  MATCHED_FINEWEB_REMOTE_ROOT_PREFIX=datasets\n"
            "  python upstream_records/records/track_10min_16mb/2026-04-18_PR1626_CaseOps_Taper/cached_challenge_fineweb.py "
            "--variant sp8192_lossless_caps_caseops_v1_reserved --train-shards 1",
            file=sys.stderr,
        )
        return 1

    env = os.environ.copy()
    run_id = env.get(
        "RUN_ID",
        f"caseops-candidate2060-{profile}-{datetime.now().strftime('%Y%m%d-%H%M%S')}",
    )
    env.setdefault("RUN_ID", run_id)
    env.setdefault("DATA_PATH", str(DATASET_DIR))
    env.setdefault("TOKENIZER_PATH", str(TOKENIZER_PATH))
    env.setdefault("VOCAB_SIZE", "8192")
    env.setdefault("LOG_CODE_SNAPSHOT", "0")
    env.setdefault("LOG_NVIDIA_SMI", "0")
    env.setdefault("PYTHONUNBUFFERED", "1")

    for key, value in PROFILES[profile].items():
        env.setdefault(key, value)

    cmd = [sys.executable, "-u", str(TRAINER)]
    print(f"profile={profile}")
    print(f"run_id={env['RUN_ID']}")
    print(f"trainer={TRAINER}")
    print(f"dataset_dir={env['DATA_PATH']}")
    print(f"tokenizer_path={env['TOKENIZER_PATH']}")
    print(f"cuda2060_profile={env['CUDA2060_PROFILE']}")
    print("launching...")
    return subprocess.run(cmd, cwd=str(ROOT), env=env).returncode


if __name__ == "__main__":
    raise SystemExit(main())
